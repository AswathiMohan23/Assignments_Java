package com.java;

public class DefaultValuesOfPrimitiveDataTypes {
    static int intValue;
    static short shortValue;
    static long longValue;
    static double doubleVal;
    static byte byteValue;
    static float floatValue;
    static char characterVal;

    public static void main(String[] args){
        System.out.println("the default values of primitive data types are : ");
        System.out.println("integer data type: "+intValue);
        System.out.println("Short dataType : "+shortValue);
        System.out.println("long data type : " +longValue);
        System.out.println("byte datatype : "+byteValue);
        System.out.println("char data type : "+characterVal);
        System.out.println("float data type : "+floatValue);
        System.out.println("double data type : " +doubleVal);


    }
}
